    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {

            jTable1.setEnabled(false);
            jButton2.setEnabled(false);
            jButton1.setEnabled(true);
            jButton3.setEnabled(true);
            jTextField3.setEnabled(false);
            jButton4.setVisible(true);

            int selectedRow = jTable1.getSelectedRow();

            String fname = String.valueOf(jTable1.getValueAt(selectedRow, 1));
            jTextField1.setText(fname);
            String lname = String.valueOf(jTable1.getValueAt(selectedRow, 2));
            jTextField2.setText(lname);
            String email = String.valueOf(jTable1.getValueAt(selectedRow, 3));
            jemail.setText(email);
            String mobile = String.valueOf(jTable1.getValueAt(selectedRow, 4));
            jmobile.setText(mobile);
            String address = String.valueOf(jTable1.getValueAt(selectedRow, 5));
            jaddress.setText(address);

            String gender = String.valueOf(jTable1.getValueAt(selectedRow, 6));

            if (gender.equals("Male")) {
                jRadioButton2.setSelected(true);
            } else {
                jRadioButton1.setSelected(true);
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

