        ButtonModel genderSelection = buttonGroup1.getSelection();

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter  First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter  Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter  Email ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!mobile.matches("^07[012456789]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter  Valid Mobile ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter  Address ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (genderSelection == null) {
            JOptionPane.showMessageDialog(this, "Please Select Gender ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {
                String gender = genderSelection.getActionCommand();

                MYSQL.executeIUD("INSERT INTO `teacher` (`first_name` , `last_name` , `email` , `mobile` , `address` , `gender_id` , `teacher_status`  )"
                        + " VALUES('" + fname + "' , '" + lname + "' , '" + email + "' , '" + mobile + "' , '" + address + "' , '" + gender + "' , 'Active'  ) ");

                reset();
                loadUser();

                JOptionPane.showMessageDialog(this, "Registration SuccessFull ", "Warning", JOptionPane.WARNING_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


