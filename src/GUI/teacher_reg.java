    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String fname = jTextField1.getText();

        if (fname.isEmpty()) {
            reset();
            loadUser();
            JOptionPane.showMessageDialog(this, "Please Select A Data From Table", "Warning", JOptionPane.WARNING_MESSAGE);
            jButton4.setVisible(false);
        } else {
            int selectedRow = jTable1.getSelectedRow();
            String id = String.valueOf(jTable1.getValueAt(selectedRow, 0));

            String lname = jTextField2.getText();
            String email = jemail.getText();
            String mobile = jmobile.getText();
            String address = jaddress.getText();
            ButtonModel genderSelection = buttonGroup1.getSelection();

            if (fname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter  First Name", "Warning", JOptionPane.WARNING_MESSAGE);
                buttonGroup1.clearSelection();
            } else if (lname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter  Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter  Email ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (!mobile.matches("^07[012456789]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Please enter  Valid Mobile ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter  Address ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (genderSelection == null) {
                JOptionPane.showMessageDialog(this, "Please Select Gender ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                try {

                    String gender = genderSelection.getActionCommand();
                    MYSQL.executeIUD("UPDATE `teacher` SET `first_name` = '" + fname + "' , `last_name` = '" + lname + "' , `email` = '" + email + "' , `mobile` = '" + mobile + "',  "
                            + " `address` = '" + address + "' , `gender_id` = '" + gender + "'  WHERE `id` = '" + id + "'  ");

                    reset();
                    loadUser();

                    JOptionPane.showMessageDialog(this, "Update Details Successfully ", "Warning", JOptionPane.WARNING_MESSAGE);
                    jButton2.setEnabled(true);
                    jButton1.setEnabled(true);

                    jButton3.setEnabled(false);
                    jButton1.setEnabled(false);
                    jTable1.setEnabled(true);
                    jTextField3.setEnabled(true);
                    jButton4.setVisible(false);

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

